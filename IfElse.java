public class IfElse {
    public static void main(String[] args) {
        int speed = 50;
        if(speed < 30){
            System.out.println("increase speed");
        }
        if(speed == 50){
            System.out.println("good speed");
        }
        if(speed > 60){
            System.out.println("stop the car");
        }
    }
}