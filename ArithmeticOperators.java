public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 8;
        int b = 7;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(++a);
    }
}